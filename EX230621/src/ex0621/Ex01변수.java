package ex0621;

public class Ex01변수 {

	public static void main(String[] args) {
		// 변수는 변하거나 변할 수 있는 수
		// Java에서는 '=' 대입, '==' 같다
		
		// 변수 선언, 할당(초기화), 재할당
		// 1. 변수 선언
		int num;
		
		// 2. 할당(초기화)
		num = 10;
		System.out.println(num);
		
		// 3. 재할당
		num = 20;
		System.out.println(num);
		
		// 4. 선언하면서 초기화하기
		int num2 = 30;
		
		// 변수를 왜 사용해야하는가?? 
		int num3 = 1000;
		
		System.out.println(num3 + 2);
		System.out.println(num3 - 3);
		System.out.println(num3 / 3);
		System.out.println(num3 * 3);
		
		
		
		
	}

}
