package 연산자;

public class Ex01대입연산자 {

	public static void main(String[] args) {
		
		// 대입연산자 -> =
		// 대입연산자 기준으로 오른쪽에서 왼쪽에 대입한다
		int num = 10;
		int num2 = num + 3;
		
		int num3 = 7; // 7
		num3 = num3 + 3; // 10
		// 복합대입연산자
		// 나 자신한테 연산 후 다시 나 자신한테 대입하기
		num3 += 3; // 13
		num3 *= 3; //39
		
		
		
		
		

	}

}
