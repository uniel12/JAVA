package 연산자;

public class Ex02비교연산자 {

	public static void main(String[] args) {

		// 비교연산자
		// 초과, 미만, 이상, 이하, 같다, 같지않다
		// > , < ,  >=,  ,<= , == , !=
		// 결과값 : 참 혹은 거짓(true or false) -> boolean
		int a = 3;
		int b = 10;
		
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a==b);
		System.out.println(a!=b);
		
	}

}
