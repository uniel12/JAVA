package 연산자;

public class Ex04증감연산자 {

	public static void main(String[] args) {
		
		// 증감연산자(단항 연산자)  ++, --
		int opr = 0;
		opr =+ 3;
		System.out.println(opr++); // 출력후 더해주기 // 3
		System.out.println(opr); // 4
		System.out.println(++opr); // 더해주고 출력하기 //5
		System.out.println(opr); // 5
		System.out.println(opr--); // 5
		System.out.println(opr); // 4
		System.out.println(--opr); // 3
		System.out.println(opr); // 3
		
		
		
		
		

	}

}
