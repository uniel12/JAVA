
public class Ex05실습5 {

	public static void main(String[] args) {
		
		
		//구구단 2단~9단( 단 가로로 출력)
		
		for (int k = 2; k <= 9; k++) {
			
			System.out.print(k+"단 : ");
			for(int i =1; i <= 9; i++) {
				System.out.print(k + "*" + i + "=" + k*i + "\t");
			}
			System.out.println();
			
		}
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
